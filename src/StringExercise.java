

public class StringExercise {
    public static void main(String[] args) {
        String noEducation = "We don't need no education\nWe don't need no thought control";
        System.out.println(noEducation);

        String quotes = "Check \"this\" out!, \"s inside of \"s";
        System.out.println(quotes);

        String windowDrive = "In windows, the main drive is usually C:\\";
        System.out.println(windowDrive);


        String slashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(slashes);
    }
}
